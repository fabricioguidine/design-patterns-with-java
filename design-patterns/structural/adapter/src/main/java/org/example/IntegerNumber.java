package org.example;

/**
 * Represents an integer number.
 * 
 * @version 1.0
 */
public class IntegerNumber {
    private int num;

    /**
     * Gets the integer value.
     * 
     * @return the integer value
     */
    public int getNum() {
        return num;
    }

    /**
     * Sets the integer value.
     * 
     * @param num the integer value to set
     */
    public void setNum(int num) {
        this.num = num;
    }
}

